package com.matheus;

public class DadosEntrada {
    private double salario;
    private double descontos;
    private int dependentes;

    public double getSalario() { return salario; }
    public void setSalario(double salario) { this.salario = salario; }

    public double getDescontos() { return descontos; }
    public void setDescontos(double descontos) { this.descontos = descontos; }

    public int getDependentes() { return dependentes; }
    public void setDependentes(int dependentes) { this.dependentes = dependentes; }
}
